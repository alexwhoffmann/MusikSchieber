package ms5000.audio.player;

/**
 * Enum to define the different states of the audio player
 */
public enum PlayerStatus {
	/**
	 * Status Playing
	 */
	PLAYING, 
	
	/**
	 * Status Paused
	 */
	PAUSED, 
	
	/**
	 * Status Player ready
	 */
	READY, 
	
	/**
	 * Status No media file in player
	 */
	NOMEDIAFILE
}
